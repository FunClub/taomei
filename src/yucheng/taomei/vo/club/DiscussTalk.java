package yucheng.taomei.vo.club;

public class DiscussTalk {
	private int users_id;
	private String headPicture;
	private String nickName;
	private String age;
	private String height;
	private String city;
	private String town;
	private String disscuss;
	private String discussTime;
	public int getUsers_id() {
		return users_id;
	}
	public void setUsers_id(int users_id) {
		this.users_id = users_id;
	}
	
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getTown() {
		return town;
	}
	public void setTown(String town) {
		this.town = town;
	}
	public String getDisscuss() {
		return disscuss;
	}
	public void setDisscuss(String disscuss) {
		this.disscuss = disscuss;
	}
	public String getDiscussTime() {
		return discussTime;
	}
	public void setDiscussTime(String discussTime) {
		this.discussTime = discussTime;
	}
	public String getHeadPicture() {
		return headPicture;
	}
	public void setHeadPicture(String headPicture) {
		this.headPicture = headPicture;
	}
	
}
